DrawRef = Tuple[int, int, int, int]  # rel_x, rel_y, bank, tile

# ---------------------------------------------------------------------------
# Camoto/Frenkel SAMLEV-derived Secret Agent mapping.
# A code maps to a 4x3 matrix anchored at the current map cell. Matrix index 11
# is the current cell; index 0 is x-3,y-2. Only non-empty entries are stored.
# ---------------------------------------------------------------------------

def _spec(idx: int, bank: int, tile: int) -> DrawRef:
    return (idx % 4 - 3, idx // 4 - 2, bank, tile)


def _br(bank: int, tile: int) -> List[DrawRef]:
    return [_spec(11, bank, tile)]


def _m(*items: Tuple[int, int, int]) -> List[DrawRef]:
    return [_spec(i, b, t) for i, b, t in items]


